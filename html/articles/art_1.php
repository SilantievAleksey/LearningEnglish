            <h1>Английский язык для начинающих</h1>
            <p>Давно минуло то время, когда английский относился к разряду необязательных, если не сказать второсортных предметов. Меняются времена и жизненные реалии. Сегодня английский язык - не роскошь, а насущная острая необходимость.</p>
            <p>Итак, если вы полны решимости оседлать эту "непокорную лошадь",  то для начала нужно выбрать не только эффективный, но и кратчайший путь к достижению своей цели.</p>
            <h2>Так ли важен психологический настрой?</h2>
            <p>Да, английский язык для начинающих не будет казаться непосильной ношей, если есть мотивация и правильный психологический настрой.  Бояться трудностей не надо, все они решаемы. Конечно, любой язык требует приложения определенных усилий, но подумайте, разве стал бы английский международным средством общения, если бы все было настолько «запущено»? Разве смог бы он распространиться из Великобритании в Австралию, Индию, Африку, Америку, если бы был неприступным?  Если уж люди осваивают такие языки, как японский, хинди, эускара (язык басков), то сложность английского в сравнении с ними кажется ничтожной. Запаситесь терпением и временем - они вам понадобятся и не паникуйте!</p>
            <p>Кроме того, как советуют психологи, для мотивации будет очень полезно представлять, то есть визуализировать конечный результат. Да, это сейчас вы в начале вашего пути. Но представьте, какие огромные возможности будут открыты перед вами, когда вы сможете понимать и свободно говорить на английском? Вы обретете настоящую свободу! </p>
